import { readFile, writeFile } from "node:fs/promises";
import { pathToFileURL } from "node:url";

const SOURCE_FILES = [
  "src/ui-core.js",
  "src/release-core.js",
  "src/catalog-core.js",
  "src/release-page.js",
  "src/anime.js",
  "src/router.js"
];
const OUTPUT_FILE = "src/app-runtime.js";
const DATA_IMPORTS = [
  "animeInfo",
  "bookItems",
  "bookItemsById",
  "catalogCoreItemOrder",
  "catalogCoreItems",
  "catalogCoreItemsById",
  "gameItems",
  "gameItemsById",
  "partItems",
  "productItems",
  "productItemsById",
  "rareBeyGetItems",
  "searchIndexItems",
  "toolsItemOrder",
  "toolsItems",
  "toolsItemsById"
];
const HEADER = [
  "// Generated by scripts/build-app-runtime.mjs. Edit the source modules listed there.",
  "import {",
  DATA_IMPORTS.map(name => `  ${name}`).join(",\n"),
  "} from \"./data-store.js\";",
  ""
].join("\n");

const runtimeSource = async () => {
  const sections = await Promise.all(SOURCE_FILES.map(async file => {
    const source = await readFile(new URL(`../${file}`, import.meta.url), "utf8");
    return `// ---- ${file} ----\n${source.trim()}\n`;
  }));
  return `${HEADER}\n${sections.join("\n")}`;
};

export async function buildAppRuntime() {
  await writeFile(new URL(`../${OUTPUT_FILE}`, import.meta.url), await runtimeSource());
}

export async function checkAppRuntime() {
  let current = "";
  try {
    current = await readFile(new URL(`../${OUTPUT_FILE}`, import.meta.url), "utf8");
  } catch {
    // A missing generated file is reported with the same actionable error.
  }
  if (current !== await runtimeSource()) {
    throw new Error(`${OUTPUT_FILE} is stale. Run pnpm app:build.`);
  }
}

const isDirectRun = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isDirectRun) {
  const command = process.argv[2] || "--check";
  if (command === "--build") {
    await buildAppRuntime();
    console.log("App runtime rebuilt.");
  } else if (command === "--check") {
    await checkAppRuntime();
    console.log("App runtime is current.");
  } else {
    throw new Error(`Unknown command: ${command}`);
  }
}
